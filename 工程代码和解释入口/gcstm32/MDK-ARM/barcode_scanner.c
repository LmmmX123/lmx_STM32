#include <string.h>

// ??????(??main.c,??????)
extern UART_HandleTypeDef huart1;   // ????
extern UART_HandleTypeDef huart3;   // ? GM65 ?????

// ???(??????)
#define MAX_LEN         128     // ??????
#define DROP_LEN        7       // ?????????
#define TOTAL_TIMEOUT_MS 6000   // ?????(??)

static char buf[MAX_LEN];        // ????,??????
char g_barcode_result[MAX_LEN] = {0};  // ??:????,????????

char* GM65_ScanOnce(void)
{
    memset(buf, 0, MAX_LEN);

    // 1. ??????(?CRC??,????)
    uint8_t cmd[] = {0x7E, 0x00, 0x08, 0x01, 0x00, 0x02, 0x01, 0xAB, 0xCD};
    HAL_UART_Transmit(&huart3, cmd, sizeof(cmd), 100);

    // 2. ????:???7???,??????
    uint8_t drop_cnt = 0;
    uint8_t idx = 0;
    uint32_t start_time = HAL_GetTick();

    while ((HAL_GetTick() - start_time) < TOTAL_TIMEOUT_MS)
    {
        if (__HAL_UART_GET_FLAG(&huart3, UART_FLAG_RXNE))
        {
            uint8_t byte = huart3.Instance->DR;
            if (drop_cnt < DROP_LEN)
            {
                drop_cnt++;                // ???7???
            }
            else
            {
                if (idx < MAX_LEN - 1)
                {
                    buf[idx++] = byte;     // ??????
                }
            }
        }
    }

    // 3. ???????NULL
    if (idx == 0)
    {
        memset(g_barcode_result, 0, MAX_LEN); // ??????????
        return NULL;
    }

    // 4. ??????????(??????)
    HAL_UART_Transmit(&huart1, (uint8_t*)"[Barcode]: ", 11, 100);
    HAL_UART_Transmit(&huart1, (uint8_t*)buf, idx, 100);
    HAL_UART_Transmit(&huart1, (uint8_t*)"\r\n", 2, 100);

    // ??:????????????
    memset(g_barcode_result, 0, MAX_LEN);  // ??????
    strncpy(g_barcode_result, buf, idx);   // ???????????
    g_barcode_result[idx] = '\0';          // ??????'\0'??

    return buf;  // ???????,??????
}
